
 CREATE TABLE SCHEDULE (  
    IDSCHEDULE NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,  
    SERVIZIO VARCHAR2(50) NOT NULL ,     
    DATAESECUZIONE DATE NOT NULL ,     
    ORAESECUZIONE VARCHAR2(5) NOT NULL ,     
  ESEGUITA VARCHAR2(1) NOT NULL,
  FREQUENZA VARCHAR2(15) NOT NULL,
  CONSTRAINT PK_IDSCHEDULE PRIMARY KEY (IDSCHEDULE)
   );
CREATE UNIQUE INDEX IDX_SCHEDULE_A ON SCHEDULE(ESEGUITA);