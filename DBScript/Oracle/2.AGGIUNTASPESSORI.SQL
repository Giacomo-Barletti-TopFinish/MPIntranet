
ALTER TABLE FASIPROCESSO 
ADD SPESSOREMINIMO FLOAT NULL
ADD SPESSORENOMINALE FLOAT NULL
ADD SPESSOREMASSIMO FLOAT NULL;

ALTER TABLE FASIPROCESSO_LOG
ADD SPESSOREMINIMO FLOAT NULL
ADD SPESSORENOMINALE FLOAT NULL
ADD SPESSOREMASSIMO FLOAT NULL;

 create or replace TRIGGER FASIPROCESSO_LOG
AFTER INSERT OR UPDATE ON FASIPROCESSO 
FOR EACH ROW
BEGIN
INSERT INTO FASIPROCESSO_LOG(IDFASEPROCESSO,IDPROCESSO,IDVASCA,DURATA,CORRENTE,SEQUENZA,CANCELLATO,DATAMODIFICA,UTENTEMODIFICA,SPESSOREMINIMO,SPESSORENOMINALE,SPESSOREMASSIMO)
VALUES
(:NEW.IDFASEPROCESSO,:NEW.IDPROCESSO,:NEW.IDVASCA,:NEW.DURATA,:NEW.CORRENTE,:NEW.SEQUENZA,:NEW.CANCELLATO,:NEW.DATAMODIFICA,:NEW.UTENTEMODIFICA,:NEW.SPESSOREMINIMO,:NEW.SPESSORENOMINALE,:NEW.SPESSOREMASSIMO);
END;
